package ClassTasksWithChibuzo;

public class Task4EvenNumbers {
    public static void main(String[] args) {
        int total = 0;
        for(int count = 1; count <= 10; count++){
            if (count % 2 == 0);
            count += 1;
            total = total + count;

        }
        System.out.println("The sum of the even numbers is " + total);
    }
}
